#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s2;
	int		m;

	m = 0;
	s2 = (char *)s;
	while (s2[m] != '\0')
	{
		if (s2[m] == c)
			return ((s2 + m));
		m++;
	}
	if (s2[m] == c)
		return ((s2 + m));
	return (NULL);
}
